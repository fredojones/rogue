
class Rect:
    """ Generic rectangle class.

    Attributes:
    x -- x position
    y -- y position
    width -- width of rectangle
    height -- height of rectangle
    """
    def __init__(self, x=0, y=0, width=0, height=0):
        self.x = x
        self.y = y
        self.width = width
        self.height = height
